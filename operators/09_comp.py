# >
print(5 > 4)    #True
print(4 > 5)    #False
print('apple' > 'Apple')    #True
print('Apple' > 'apple')    #False

# < 
print(8 < 7)    #False
print(9 < 5)    #False
print(2 < 10)   #True

# >=
print(8 >= 2)   #True
print(8 >= 8)   #True
print(8 >= 10)  #False

# <=
print(3 <= 9)   #True
print(3 <= 3)   #True
print(3 <= 1)   #False

# ==    
print(3 == 8)   #False
print(9 == 9)   #True

# !=
print(4 != 7)   #True
print(2 != 2)   #False
print('Apple' != "Apple")   #False
print('Apple' != 'apple')   #True
print('apple' != 'apple')   #False

